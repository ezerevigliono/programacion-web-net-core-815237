    internal class _1_programacion_orientada_a_objetos
    {

        // Diferencias entre Programación Estructurada y POO

        // La programación estructurada se basa en la descomposición de un problema en subproblemas más pequeños, que se resuelven de forma secuencial mediante funciones y procedimientos.

        // La programación orientada a objetos, en cambio, se basa en la creación de clases que encapsulan datos y comportamientos relacionados, permitiendo la creación de objetos que interactúan entre sí mediante mensajes.

        // ¿Qué son los objetos?
        // Los objetos son instancias de clases que combinan datos y comportamientos. Cada objeto es una entidad independiente que puede interactuar con otros objetos mediante mensajes.

        // ¿Qué es una clase?
        // Una clase es una plantilla que define la estructura y el comportamiento de un tipo de objeto. Las clases encapsulan los atributos y métodos que definen las propiedades y el comportamiento de los objetos.

        // ¿Qué son métodos?
        // Los métodos son funciones que definen el comportamiento de una clase. 

        // ¿Qué son propiedades?
        // Las propiedades son atributos de una clase que definen sus características. Las propiedades pueden ser de lectura, de escritura o de lectura y escritura. Una propiedad de lectura solo permite obtener el valor de un atributo. Una propiedad de escritura solo permite establecer el valor de un atributo. Una propiedad de lectura y escritura permite obtener y establecer el valor de un atributo.

        // Los principios básicos de la POO son:

        // 1. Abstracción: La abstracción consiste en modelar el mundo real mediante la creación de clases que representan entidades abstractas. Estas clases encapsulan los atributos y comportamientos relevantes de las entidades, proporcionando una vista simplificada y esencial de su funcionamiento.

        // 2. Encapsulación: La encapsulación oculta los detalles internos de una clase y solo expone un conjunto de métodos públicos para interactuar con ella. Esto promueve la modularidad y protege los datos internos de modificaciones no deseadas.

        // 3. Ocultamiento : El ocultamiento es un principio de la programación orientada a objetos que consiste en ocultar los detalles de implementación de una clase y exponer solo la interfaz pública. Esto permite proteger los datos internos de la clase y facilita la reutilización del código.

        // 4. Herencia: La herencia permite crear nuevas clases a partir de clases existentes, heredando sus atributos y comportamientos. Esto promueve la reutilización de código y facilita la creación de jerarquías de clases que reflejan relaciones del mundo real.

        // 5. Polimorfismo: El polimorfismo permite que diferentes clases respondan al mismo mensaje de manera distinta. Esto aporta flexibilidad y dinamismo al código, ya que las clases pueden adaptar su comportamiento según el contexto.
    }
}
